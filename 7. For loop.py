# Example - For loop
name = "SANJANA"

# It is going to repeat itself until the contents inside the loop is over
# Each character is picked one after the other and then it is printed in this case
for loop_name in name:
    print(loop_name)

# Example - for a list
name1 = ["SANJANA", "SOMESH", "KHUSHI", "DRISHTI", "ANIRUDH"]

for names in name1:
    print(names)

for names in name1:
    print(names[0])

